    on inventory_transactions;

create trigger trg_inventory_transactions_validate_master_data
before insert or update on inventory_transactions
for each row
execute function inventory_validate_transaction_master_data();

create or replace function inventory_prevent_negative_stock()
returns trigger
language plpgsql
as $$
declare
    v_product_ids bigint[];
    v_product_id bigint;
    v_balance numeric(14, 3);
begin
    if tg_op = 'DELETE' then
        v_product_ids := array[old.product_id];
    elsif tg_op = 'UPDATE' then
        v_product_ids := array(
            select distinct unnest(array[old.product_id, new.product_id])
        );
    else
        v_product_ids := array[new.product_id];
    end if;

    foreach v_product_id in array v_product_ids loop
        perform pg_advisory_xact_lock(v_product_id);

        select coalesce(sum(quantity_delta), 0)
          into v_balance
          from inventory_transactions
         where product_id = v_product_id;

        if v_balance < 0 then
            raise exception
                'Inventory transaction would create negative stock for product_id %. Resulting balance %',
                v_product_id,
                v_balance
                using errcode = '23514';
        end if;
    end loop;

    if tg_op = 'DELETE' then
        return old;
    end if;

    return new;
end;
$$;

drop trigger if exists trg_inventory_transactions_no_negative_stock
    on inventory_transactions;

create trigger trg_inventory_transactions_no_negative_stock
after insert or update or delete on inventory_transactions
for each row
execute function inventory_prevent_negative_stock();

create or replace view vw_inventory_stock_balance as
select
    p.id as product_id,
    p.product_name,
    p.original_product_name,
    c.category_name,
    s.subcategory_name,
    p.base_unit,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'OPENING_STOCK'
    ), 0)::numeric(14, 3) as opening_stock,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'PURCHASE'
    ), 0)::numeric(14, 3) as purchased,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'RETURN_IN'
    ), 0)::numeric(14, 3) as returned_in,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'USAGE'
    ), 0)::numeric(14, 3) as used,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'WASTE_EXPIRED'
    ), 0)::numeric(14, 3) as wasted_expired,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'ADJUSTMENT_INCREASE'
    ), 0)::numeric(14, 3) as adjustment_in,
    coalesce(sum(t.quantity) filter (
        where t.transaction_type = 'ADJUSTMENT_DECREASE'
    ), 0)::numeric(14, 3) as adjustment_out,
    coalesce(sum(t.quantity_delta), 0)::numeric(14, 3) as current_stock,
    max(t.transaction_date) as latest_transaction_date,
    max(t.created_at) as latest_transaction_created_at,
    min(t.expiry_date) filter (
        where t.expiry_date is not null
          and t.quantity_delta > 0
          and t.expiry_date >= current_date
    ) as nearest_unexpired_expiry_date,
    coalesce(
        bool_or(t.expiry_date is not null and t.expiry_date < current_date),
        false
    ) as has_expired_lot_history,
    p.active
from inventory_products p
join inventory_categories c
  on c.id = p.category_id
join inventory_subcategories s
  on s.id = p.subcategory_id
left join inventory_transactions t
  on t.product_id = p.id
group by
    p.id,
    p.product_name,
    p.original_product_name,
    c.category_name,
    s.subcategory_name,
    p.base_unit,
    p.active;

commit;
