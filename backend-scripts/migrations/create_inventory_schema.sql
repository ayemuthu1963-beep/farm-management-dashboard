create or replace function inventory_validate_transaction_master_data()
returns trigger
language plpgsql
as $$
declare
    v_product_name text;
    v_base_unit text;
    v_active boolean;
begin
    select product_name, base_unit, active
      into v_product_name, v_base_unit, v_active
      from inventory_products
     where id = new.product_id;

    if not found then
        raise exception 'Inventory product_id % does not exist', new.product_id
            using errcode = '23503';
    end if;

    if v_active is not true then
        raise exception 'Inventory product % is inactive and cannot receive transactions', v_product_name
            using errcode = '23514';
    end if;

    if v_base_unit = 'to_confirm' then
        raise exception 'Inventory product % has base_unit to_confirm; confirm base unit before transactions', v_product_name
            using errcode = '23514';
    end if;

    if new.unit <> v_base_unit then
        raise exception 'Inventory transaction unit % does not match product base_unit % for product %',
            new.unit,
            v_base_unit,
            v_product_name
            using errcode = '23514';
    end if;

    return new;
end;
$$;

