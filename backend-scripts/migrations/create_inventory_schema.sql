begin;

create table if not exists inventory_categories (
    id bigint generated by default as identity primary key,
    category_name text not null,
    description text null,
    active boolean not null default true,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    created_by text null,
    updated_by text null,

    constraint inventory_categories_name_not_blank_check
        check (length(btrim(category_name)) > 0)
);

create unique index if not exists idx_inventory_categories_name_ci
    on inventory_categories (lower(btrim(category_name)));

create table if not exists inventory_subcategories (
    id bigint generated by default as identity primary key,
    category_id bigint not null
        references inventory_categories(id)
        on update cascade
        on delete restrict,
    subcategory_name text not null,
    description text null,
    active boolean not null default true,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    created_by text null,
    updated_by text null,

    constraint inventory_subcategories_name_not_blank_check
        check (length(btrim(subcategory_name)) > 0)
);

create unique index if not exists idx_inventory_subcategories_category_name_ci
    on inventory_subcategories (category_id, lower(btrim(subcategory_name)));

create unique index if not exists idx_inventory_subcategories_category_id_id
    on inventory_subcategories (category_id, id);

create index if not exists idx_inventory_subcategories_category_id
    on inventory_subcategories (category_id);

create table if not exists inventory_products (
    id bigint generated by default as identity primary key,
    product_name text not null,
    original_product_name text null,
    category_id bigint not null
        references inventory_categories(id)
        on update cascade
        on delete restrict,
    subcategory_id bigint not null
        references inventory_subcategories(id)
        on update cascade
        on delete restrict,
    base_unit text not null,
    reorder_level numeric(14, 3) null,
    expiry_required boolean not null default false,
    active boolean not null default true,
    remarks text null,
    source text not null default 'Manual_Admin',
    source_reference text null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    created_by text null,
    updated_by text null,

    constraint inventory_products_name_not_blank_check
        check (length(btrim(product_name)) > 0),

    constraint inventory_products_base_unit_check
        check (base_unit in ('kg', 'g', 'L', 'ml', 'number', 'to_confirm')),

    constraint inventory_products_reorder_level_check
        check (reorder_level is null or reorder_level >= 0),

    constraint inventory_products_source_check
        check (source in ('Manual_Admin', 'INITIAL_EXCEL_IMPORT', 'ODK', 'Import', 'System')),

    constraint inventory_products_subcategory_matches_category_fk
        foreign key (category_id, subcategory_id)
        references inventory_subcategories(category_id, id)
        on update cascade
        on delete restrict
);

create unique index if not exists idx_inventory_products_name_ci
    on inventory_products (lower(btrim(product_name)));

create unique index if not exists idx_inventory_products_source_reference
    on inventory_products (source, source_reference)
    where source_reference is not null;

create index if not exists idx_inventory_products_category_subcategory
    on inventory_products (category_id, subcategory_id);

create index if not exists idx_inventory_products_active
    on inventory_products (active);

create table if not exists inventory_transactions (
    id bigint generated by default as identity primary key,
    product_id bigint not null
        references inventory_products(id)
        on update cascade
        on delete restrict,
    transaction_date date not null,
    transaction_type text not null,
    quantity numeric(14, 3) not null,
    quantity_delta numeric(14, 3) generated always as (
        case
            when transaction_type in (
                'OPENING_STOCK',
                'PURCHASE',
                'RETURN_IN',
                'ADJUSTMENT_INCREASE'
            ) then quantity
            when transaction_type in (
                'USAGE',
                'WASTE_EXPIRED',
                'ADJUSTMENT_DECREASE'
            ) then -quantity
            else 0
        end
    ) stored,
    unit text not null,
    expiry_date date null,
    batch_no text null,
    supplier text null,
    reference_no text null,
    invoice_no text null,
    purpose text null,
    plot_zone text null,
    crop text null,
    source text not null default 'Manual_Admin',
    source_reference text null,
    entered_by text null,
    remarks text null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    created_by text null,
    updated_by text null,

    constraint inventory_transactions_type_check
        check (transaction_type in (
            'OPENING_STOCK',
            'PURCHASE',
            'USAGE',
            'RETURN_IN',
            'WASTE_EXPIRED',
            'ADJUSTMENT_INCREASE',
            'ADJUSTMENT_DECREASE'
        )),

    constraint inventory_transactions_quantity_positive_check
        check (quantity > 0),

    constraint inventory_transactions_unit_check
        check (unit in ('kg', 'g', 'L', 'ml', 'number')),

    constraint inventory_transactions_source_check
        check (source in ('Manual_Admin', 'INITIAL_EXCEL_IMPORT', 'ODK', 'Import', 'System')),

    constraint inventory_transactions_usage_details_required_check
        check (
            transaction_type <> 'USAGE'
            or (
                purpose is not null
                and length(btrim(purpose)) > 0
                and plot_zone is not null
                and length(btrim(plot_zone)) > 0
                and crop is not null
                and length(btrim(crop)) > 0
            )
        )
);

create index if not exists idx_inventory_transactions_product_date
    on inventory_transactions (product_id, transaction_date desc, id desc);

create index if not exists idx_inventory_transactions_type_date
    on inventory_transactions (transaction_type, transaction_date desc, id desc);

create unique index if not exists idx_inventory_transactions_source_reference
    on inventory_transactions (source, source_reference)
    where source_reference is not null;

create index if not exists idx_inventory_transactions_expiry_date
    on inventory_transactions (expiry_date)
    where expiry_date is not null;

create index if not exists idx_inventory_transactions_invoice_no
    on inventory_transactions (invoice_no)
    where invoice_no is not null;

